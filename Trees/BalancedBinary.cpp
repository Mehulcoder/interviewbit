/**
 * Definition for binary tree
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode(int x) : val(x), left(NULL), right(NULL) {}
 * };
 */
int depth(TreeNode* node){
    if (node == NULL)
        return 0;
    return 1 + max(depth(node->left), depth(node->right));
}
int Solution::isBalanced(TreeNode* A) {
    int le = 0, ri = 0;
    if (A == NULL) return 1;
    le = depth(A->left);
    ri = depth(A->right);
    if (abs(le - ri) < 2 && isBalanced(A->left) && isBalanced(A->right))
        return 1;
    return 0;
}